# Simple variables and printing
x = 4
print(x)  # Output: 4

y = "Hello World"
print(y)  # Output: Hello World

# -----------------------------
# Data types using constructors

a = str("hello")   # string
b = int(3)         # integer
c = float(3)       # float

print(a)  # Output: hello
print(b)  # Output: 3
print(c)  # Output: 3.0

# -----------------------------
# Find data type using type()

x = 5
y = "John"

print(type(x))  # Output: <class 'int'>
print(type(y))  # Output: <class 'str'>

# -----------------------------
# Unpacking a list into variables

fruits = ["apple", "banana", "cherry"]
x, y, z = fruits

print(x)  # Output: apple
print(y)  # Output: banana
print(z)  # Output: cherry

# -----------------------------
# Arithmetic with numbers

x = 5
y = 10
print(x + y)  # Output: 15

# -----------------------------
# String concatenation

x = "Ved"
y = "Darji"
print(x + y)  # Output: VedDarji
